const mongoose = require("mongoose");

const adminSchema = mongoose.Schema({
  email: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  type: {
    type: String,
    required: true,
  },
});

const adminModel = mongoose.model("User", adminSchema);

module.exports = adminModel;
